           <p align="justify"><b>Age eligibility </b>(As on 1st April of the academic session for which admission is sought)<br />
(A) Nursery &#8211; 3 years<br />
(B) L.K.G &#8211; 4 years<br />
(C) U.K.G &#8211; 5 years<br />
(D) Class I &#8211; 6 years<br />
(E) Class II onwards &#8211; on the basis of previous class<br />
Children in the Hostel are admitted from age 5 onwards.</p>
<p><b>Registration </b>&#8211; A duly filled form available from the school/website is to be submitted with the non-refundable, non-transferable Registration Fee within 7 days from the date of issue of form or the last date given.<br />
(A) A copy of birth certificate must be attached with the registration form.<br />
(B) Students who have attended other schools need to submit a photocopy of the report card of the last two years from the previous school.</p>
<p>PLEASE NOTE THAT THE REGISTRATION DOES NOT IN ANY WAY GUARANTEE ADMISSION.</p>
<p><b>Admission Test:</b><br />
The children who have registered will have to appear for an admission test (for class III onwards), which may be written or oral as laid down by the School for different levels. The test will be held for English, Hindi, Mathematics and/or any other subject which may be considered necessary.</p>
